# Creat a Band Name Generator using strings and variables

# Printing a string that is the welcome message
print("Welcome to the Band Name Generator!")
# Taking input from the user that is stored in the city variable.
city = input("What city were you born in?:\n> ")
# Taking input from the user that is stored in the pet variable.
pet = input("What's your pet's name?:\n> ")
# Printing their suggested band name as an f-string adding the city and pet variables.
print(f"Your band name could be {city} {pet}.")